"""DR-runbook generator — deterministic Markdown for the four W5 drills (W5-T5).

ADR-0030 §5 / PRODUCTION.md §8 require the DR runbook to be GENERATED and kept
current, not hand-maintained — the Documentation Agent's dogfooding mandate. The
Documentation Agent ships two GENERATION modes (ADR-0019 §2/§3): a deterministic,
no-LLM renderer (network inventories, topology diagrams — "output matches the
source content exactly by construction") and an LLM-narrative renderer (per-device
runbooks). DR DRILL runbooks are operational procedures grounded in the chart's
authoritative drill facts (the rendered Job/CronJob + the per-tier `DRILL ...`
contract), NOT per-device narratives — so they are generated by the SAME
deterministic, no-LLM path the inventory tool uses. The facts below come straight
from the drill manifests + the per-tier harness contracts (one source of truth);
the renderer interpolates them into a stable template, so re-running it on a
config change regenerates a current runbook with zero drift and zero LLM calls.

OFFLINE-HONESTY (P1, this host): the Documentation Agent's LLM-narrative runbook
path (`app.agents.documentation.tools.generate_runbook`) needs a reachable
``BaseChatModel`` provider; none is available on the P1 build host (no Ollama / no
API key). Per the W5-T5 spec, we therefore commit (a) this deterministic
generation wiring and (b) the seeded runbooks it produces — and say so in the
runbook header + the G-REL evidence doc. We do NOT fabricate LLM-narrative output.
When a provider is wired in P2, the narrative-grounding sections can be layered on
top of these deterministic procedure tables (the facts stay the source of truth).

Run:  python -m full_platform.run_drill ...           # runs the drill
      python -m full_platform.runbook --out docs/runbooks   # (re)generates docs
"""

from __future__ import annotations

import argparse
import sys
from collections.abc import Sequence
from dataclasses import dataclass, field
from pathlib import Path

#: Header stamped on every generated runbook so a reader knows it is generated.
_GENERATED_BANNER = (
    "> **GENERATED — do not edit by hand.** Produced by "
    "`full_platform.runbook` (W5-T5, deterministic / no-LLM, ADR-0019 §2 dogfooding "
    "mode). Re-run the generator after any drill config change to keep it current "
    "(freshness target <= 90 days once drills run in P2 — G-OBS).\n>\n"
    "> The Documentation Agent's LLM-narrative runbook path "
    "(`app.agents.documentation.tools.generate_runbook`) needs a reachable LLM "
    "provider, which the P1 build host does not have — so the narrative layer is "
    "deferred to P2 and these runbooks carry the deterministic procedure tables "
    "only (W5-T5 spec: no fabricated generated output)."
)


@dataclass(frozen=True)
class DrillRunbook:
    """The authoritative facts for one drill's runbook (one source of truth)."""

    slug: str
    title: str
    adr: str
    cadence: str
    objective: str
    backup_type: str
    job_name: str
    restore_source: str
    restore_target: str
    proves: list[str]
    drill_lines: list[str]
    steps: list[str]
    failure_modes: list[tuple[str, str]] = field(default_factory=list)

    def render(self) -> str:
        out: list[str] = [f"# {self.title}", "", _GENERATED_BANNER, ""]
        out += ["## Objective", "", self.objective, ""]
        out += [
            "## Facts",
            "",
            "| Field | Value |",
            "|---|---|",
            f"| ADR | {self.adr} |",
            f"| Cadence (P2) | {self.cadence} |",
            f"| Backup-type label | `{self.backup_type}` |",
            f"| Job / CronJob | `{self.job_name}` |",
            f"| Restore source | {self.restore_source} |",
            f"| Restore target | {self.restore_target} |",
            "| Execution phase | P2 (built P1, suspended CronJob) |",
            "",
        ]
        out += ["## What this drill proves", ""]
        out += [f"- {p}" for p in self.proves]
        out += [""]
        out += ["## Procedure", ""]
        out += [f"{i}. {s}" for i, s in enumerate(self.steps, start=1)]
        out += [""]
        out += [
            "## Structured evidence (the `DRILL ...` lines the collector parses)",
            "",
            "```",
            *self.drill_lines,
            "```",
            "",
        ]
        if self.failure_modes:
            out += ["## Failure modes & response", "", "| Symptom | Response |", "|---|---|"]
            out += [f"| {sym} | {resp} |" for sym, resp in self.failure_modes]
            out += [""]
        out += [
            "## Roll-back / safety",
            "",
            "- The drill writes ONLY to a throwaway `emptyDir` scratch torn down with "
            "the pod; it mounts no live PVC, so there is nothing to roll back and no "
            "production data is touched (ADR-0030 §5.3).",
            "- To stop a P2 run mid-flight: `kubectl delete job "
            f"{self.job_name}` (the CronJob stays suspended until deliberately "
            "un-suspended).",
            "",
        ]
        return "\n".join(out)


