ALL_RUNBOOKS: list[DrillRunbook] = [_PITR, _NEO4J, _PCAP, _FULL]


def generate(out_dir: Path) -> list[Path]:
    """Render all four runbooks into ``out_dir`` as ``dr-<slug>.md``; return paths."""
    out_dir.mkdir(parents=True, exist_ok=True)
    written: list[Path] = []
    for rb in ALL_RUNBOOKS:
        path = out_dir / f"dr-{rb.slug}.md"
        path.write_text(rb.render() + "\n", encoding="utf-8")
        written.append(path)
    return written


def _parse_args(argv: Sequence[str] | None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(prog="full_platform.runbook")
    parser.add_argument(
        "--out",
        default="docs/runbooks",
        help="output directory for the generated dr-*.md runbooks.",
    )
    return parser.parse_args(list(argv) if argv is not None else None)


def main(argv: Sequence[str] | None = None) -> int:
    args = _parse_args(argv)
    written = generate(Path(args.out))
    for p in written:
        print(f"generated {p}")
    return 0


if __name__ == "__main__":  # pragma: no cover - exercised via the generator wrapper
    sys.exit(main())
