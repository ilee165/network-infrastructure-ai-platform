def _seed_roles_and_admin() -> None:
    timestamp = sa.func.current_timestamp()

    roles = sa.table(
        "roles",
        sa.column("id", sa.Uuid()),
        sa.column("name", sa.String()),
        sa.column("created_at", sa.DateTime(timezone=True)),
        sa.column("updated_at", sa.DateTime(timezone=True)),
    )
    op.execute(
        roles.insert().values(
            [
                {
                    "id": _seed_id("role", name),
                    "name": name,
                    "created_at": timestamp,
                    "updated_at": timestamp,
                }
                for name in _ROLE_NAMES
            ]
        )
    )

    password = os.environ.get("NETOPS_ADMIN_PASSWORD") or ""
    if not password:
        password = "admin"
        logger.warning(
            "NETOPS_ADMIN_PASSWORD is not set; seeding the 'admin' user with the insecure "
            "default password — set NETOPS_ADMIN_PASSWORD and rotate this account immediately"
        )

    users = sa.table(
        "users",
        sa.column("id", sa.Uuid()),
        sa.column("username", sa.String()),
        sa.column("password_hash", sa.String()),
        sa.column("role_id", sa.Uuid()),
        sa.column("is_active", sa.Boolean()),
        sa.column("created_at", sa.DateTime(timezone=True)),
        sa.column("updated_at", sa.DateTime(timezone=True)),
    )
    op.execute(
        users.insert().values(
            id=_seed_id("user", "admin"),
            username="admin",
            password_hash=hash_password(password),
            role_id=_seed_id("role", "admin"),
            is_active=True,
            created_at=timestamp,
            updated_at=timestamp,
        )
    )


