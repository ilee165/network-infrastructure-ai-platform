    op.create_table(
        "raw_artifacts",
        sa.Column("id", sa.Uuid(), nullable=False),
        sa.Column("created_at", sa.DateTime(timezone=True), nullable=False),
        sa.Column("device_id", sa.Uuid(), nullable=False),
        sa.Column("run_id", sa.Uuid(), nullable=True),
        sa.Column("command", sa.String(length=255), nullable=False),
        sa.Column("raw_text", sa.Text(), nullable=False),
        sa.Column("parsed", _JSON, nullable=True),
        sa.PrimaryKeyConstraint("id", "created_at", name=op.f("pk_raw_artifacts")),
        sa.ForeignKeyConstraint(
            ["device_id"], ["devices.id"], name=op.f("fk_raw_artifacts_device_id")
        ),
        sa.ForeignKeyConstraint(
            ["run_id"], ["discovery_runs.id"], name=op.f("fk_raw_artifacts_run_id")
        ),
        postgresql_partition_by="RANGE (created_at)",
    )
    op.create_index(op.f("ix_raw_artifacts_device_id"), "raw_artifacts", ["device_id"])
    op.create_index(op.f("ix_raw_artifacts_run_id"), "raw_artifacts", ["run_id"])

    if not _is_postgresql():
        return

    for parent in _PARTITIONED_TABLES:
        for suffix, lower, upper in _PARTITION_WINDOWS:
            op.execute(
                f"CREATE TABLE {parent}_{suffix} PARTITION OF {parent} "
                f"FOR VALUES FROM ('{lower}') TO ('{upper}')"
            )
        op.execute(f"CREATE TABLE {parent}_default PARTITION OF {parent} DEFAULT")

    # Append-only audit log (ADR-0011): block UPDATE/DELETE by default.
    # Caveat: REVOKE ... FROM PUBLIC cannot bind the table OWNER (or
    # superusers) — PostgreSQL owners always hold implicit privileges. Real
    # enforcement requires the application to connect as a NON-OWNER role
    # that is granted only INSERT/SELECT on audit_log (deployment concern);
    # this statement keeps any incidental PUBLIC access closed by default.
    # Partitions reached directly (not via the parent) carry their own ACLs.
    op.execute("REVOKE UPDATE, DELETE ON audit_log FROM PUBLIC")


