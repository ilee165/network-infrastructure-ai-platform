def downgrade() -> None:
    op.drop_table("normalized_neighbors")
    op.drop_table("normalized_routes")
    op.drop_table("normalized_interfaces")

    if _is_postgresql():
        # Explicitly drop partitions in reverse creation order before the
        # parents (DROP TABLE on the parent would cascade, but the reverse
        # of upgrade() is kept literal and reviewable).
        for parent in reversed(_PARTITIONED_TABLES):
            op.execute(f"DROP TABLE {parent}_default")
            for suffix, _lower, _upper in reversed(_PARTITION_WINDOWS):
                op.execute(f"DROP TABLE {parent}_{suffix}")
        # The upgrade-time REVOKE needs no inverse: PUBLIC holds no table
        # privileges by default, and the table is dropped below anyway.

    op.drop_table("raw_artifacts")
    op.drop_table("audit_log")
    op.drop_table("discovery_runs")
    op.drop_table("devices")
    op.drop_table("device_credentials")
    op.drop_table("users")
    op.drop_table("roles")
